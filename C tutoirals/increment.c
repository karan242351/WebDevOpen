#include<stdio.h>
//for loop

int main ()
{
 int i=1;
     printf("%d\n",i++);//use , then increase
     printf("%d\n",i);// increases value is printed
     printf("%d\n", ++i);//first increase, then use
    printf("%d\n",i);
    printf("%d\n",i--);//use, then decrease
    printf("%d\n",i);
     printf("%d\n",--i);
     printf("%d\n",i);
  return 0;
}